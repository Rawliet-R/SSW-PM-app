const _SORTED = Object.entries(FURIGANA_DICT).sort((a, b) => b[0].length - a[0].length);

export function applyFurigana(text) {
  if (!text) return text;
  const chars = [...text];
  const parts = [];
  let i = 0;
  while (i < chars.length) {
    let matched = false;
    for (const [kanji, reading] of _SORTED) {
      const k = [...kanji];
      const slice = chars.slice(i, i + k.length).join('');
      if (slice === kanji) {
        parts.push({ type: 'ruby', kanji, reading });
        i += k.length;
        matched = true;
        break;
      }
    }
    if (!matched) {
      if (parts.length && parts[parts.length-1].type === 'text') {
        parts[parts.length-1].text += chars[i];
      } else {
        parts.push({ type: 'text', text: chars[i] });
      }
      i++;
    }
  }
  return parts;
}

export function FuriganaText({ text, className, style }) {
  if (!text) return null;
  const parts = applyFurigana(text);
  return (
    <span className={className} style={style}>
      {parts.map((p, i) =>
        p.type === 'ruby'
          ? <ruby key={i}>{p.kanji}<rt>{p.reading}</rt></ruby>
          : <span key={i}>{p.text}</span>
      )}
    </span>
  );
}
